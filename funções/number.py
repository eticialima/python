## Tratar campos

# Converte uma string para inteiro usando int()
x = "42"
y = int(x)
print(type(y)) # <class 'int'>
print(y) # 42

# Converte um inteiro para string usando str()
x = 42
y = str(x)
print(type(y)) # <class 'str'>
print(y) # "42"

# Converte uma string para ponto flutuante usando float()
x = "3.14"
y = float(x)
print(type(y)) # <class 'float'>
print(y) # 3.14

# Converte um ponto flutuante para string usando str()
x = 3.14
y = str(x)
print(type(y)) # <class 'str'>
print(y) # "3.14"

